const ALL_REPLACEMENTS = [...REPLACEMENTS, ...PASS2_REPLACEMENTS];

function fixRegexes(s) {
  return s
    .replace(/\bIcin\b/g, "İçin")
    .replace(/\bicin\b/g, "için")
    .replace(/\bIzni\b/g, "İzni")
    .replace(/\bIstek\b/g, "İstek");
}

function fixString(s) {
  if (typeof s !== "string") return s;
  let t = s;
  for (const [from, to] of ALL_REPLACEMENTS) {
    if (t.includes(from)) t = t.split(from).join(to);
  }
  t = fixRegexes(t);
  return t;
}

function walkValues(obj, fn) {
  if (Array.isArray(obj)) {
    obj.forEach((item, i) => {
      if (typeof item === "string") obj[i] = fn(item);
      else if (item && typeof item === "object") walkValues(item, fn);
    });
  } else if (obj && typeof obj === "object") {
    for (const k of Object.keys(obj)) {
      const v = obj[k];
      if (typeof v === "string") obj[k] = fn(v);
      else if (v && typeof v === "object") walkValues(v, fn);
    }
  }
}

let n = 0;
for (const f of fs.readdirSync(TR_DIR)) {
  if (!f.endsWith(".json")) continue;
  const fp = path.join(TR_DIR, f);
  const data = JSON.parse(fs.readFileSync(fp, "utf8"));
  walkValues(data, fixString);
  fs.writeFileSync(fp, `${JSON.stringify(data, null, 2)}\n`, "utf8");
  n += 1;
}
